package FirstPackage;

public class DoubleEx1 {

	public static void main(String[] args) {
		//실수 데이터 타입 : float(4), double(8)
		//실수형 데이터는 부동 소수점 방식
		
		float frum = 3.14F; // float 데이터 표현은 마지막에 알파벳 F or f로 표현합니다.
		double dnum = 3.14; // 3.14 동일한 표현
		
		System.out.println(frum);
		System.out.println(dnum);
	}
}